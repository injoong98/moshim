        function device_check() {
            // 디바이스 종류 설정
            let pc_device = "win16|win32|win64|mac|macintel";

            // 접속한 디바이스 환경
            let this_device = navigator.platform;

            if (this_device) {
                if (pc_device.indexOf(this_device.toLowerCase()) < 0) {
                    return 'M'
                } else {
                    return 'P'
                }
            }
